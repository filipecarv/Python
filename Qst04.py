N1 = int(input('Me diga um numero ai meu bom:'))
if N1 == 5:
    print('Seu numero e 5!')
if N1 != 7:
    print('Seu numero e diferente de 7!')
if N1 > 2:
    print('Seu numero e maior que 2!')
if N1 >= 5:
    print('Seu numero e maior ou igual a 5!')
if N1 > 7:
    print('Seu numero e maior que 7 meu bom!')
if N1 <= 5:
    print('Seu numero e menor ou igual a 5 meu bom!')